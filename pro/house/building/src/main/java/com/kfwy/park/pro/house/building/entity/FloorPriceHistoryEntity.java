package com.kfwy.park.pro.house.building.entity;

import com.gniuu.framework.entity.BaseEntity;

import java.math.BigDecimal;
import java.util.Date;

/**
 * 楼层价格历史实体
 * @author wangchuang
 */
public class FloorPriceHistoryEntity extends BaseEntity{
    /**主键园区编码*/
    private String pkCode;
    /**建筑编码*/
    private String bdCode;
    /**楼层编码*/
    private String flCode;
    /**价格*/
    private BigDecimal price;
    /**价格单位*/
    private String priceUnit;
    /**定价时间*/
    private Date pricingTime;

    /*----------字段分割线-----------*/

    public String getPkCode() {
        return pkCode;
    }

    public void setPkCode(String pkCode) {
        this.pkCode = pkCode;
    }

    public String getBdCode() {
        return bdCode;
    }

    public void setBdCode(String bdCode) {
        this.bdCode = bdCode;
    }

    public String getFlCode() {
        return flCode;
    }

    public void setFlCode(String flCode) {
        this.flCode = flCode;
    }

    public BigDecimal getPrice() {
        return price;
    }

    public void setPrice(BigDecimal price) {
        this.price = price;
    }

    public String getPriceUnit() {
        return priceUnit;
    }

    public void setPriceUnit(String priceUnit) {
        this.priceUnit = priceUnit;
    }

    public Date getPricingTime() {
        return pricingTime;
    }

    public void setPricingTime(Date pricingTime) {
        this.pricingTime = pricingTime;
    }
}
