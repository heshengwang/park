package com.kfwy.park.bi.analysis.entity;

import com.gniuu.framework.entity.BaseEntity;

import java.math.BigDecimal;

/**
 * @author wangchuang
 * @Description 到期监控（主要查询某年某月到期数量）
 * @Date $date$ $time$
 * @Version 1.0
 * @Param $param$
 * @return $return$
 **/
public class MaturityEntity extends BaseEntity {

    /**
     * 到期的年
     */
    private String maturityYear;

    /**
     * 到期的月
     */
    private String maturityMonth;

    /**
     * 到期的日
     */
    private String maturityDay;

    /**
     * 到期的数量
     */
    private Integer maturityCount;

    /**
     *到期的面积
     */
    private BigDecimal maturityArea;

    /**
     *营收
     */
    private BigDecimal revenue;

    public String getMaturityYear() {
        return maturityYear;
    }

    public void setMaturityYear(String maturityYear) {
        this.maturityYear = maturityYear;
    }

    public String getMaturityMonth() {
        return maturityMonth;
    }

    public void setMaturityMonth(String maturityMonth) {
        this.maturityMonth = maturityMonth;
    }

    public String getMaturityDay() {
        return maturityDay;
    }

    public void setMaturityDay(String maturityDay) {
        this.maturityDay = maturityDay;
    }

    public Integer getMaturityCount() {
        return maturityCount;
    }

    public void setMaturityCount(Integer maturityCount) {
        this.maturityCount = maturityCount;
    }

    public BigDecimal getMaturityArea() {
        return maturityArea;
    }

    public void setMaturityArea(BigDecimal maturityArea) {
        this.maturityArea = maturityArea;
    }

    public BigDecimal getRevenue() {
        return revenue;
    }

    public void setRevenue(BigDecimal revenue) {
        this.revenue = revenue;
    }
}
