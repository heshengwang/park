package com.kfwy.park.sys.user.entity;


import com.gniuu.framework.user.entity.BaseUserEntity;

/**
 * @Description 此User是当前系统的用户登录
 * @Auth luming
 * @Date 2018/7/11 17:19
 * @Version 1.0
 * @Param
 * @return
 */
public class UserEntity extends BaseUserEntity {

    /**
     * 对应员工工号
     */
    private String workNumber;

    /**
     * 岗位编码
     */
    private String postCode;

    /**
     * 关联人员编码
     */
    private String empCode;

    /**
     * 人员姓名
     */
    private String empName;

    /**
     * 人员部门
     */
    private String deptCode;

    /**
     * 人员部门名称
     */
    private String deptName;

    /**
     * 公司代号
     */
    private String cpyMark;

    /*---------------------------------------------get set方法---------------------------------------------*/

    public String getWorkNumber() {
        return workNumber;
    }

    public void setWorkNumber(String workNumber) {
        this.workNumber = workNumber;
    }

    public String getPostCode() {
        return postCode;
    }

    public void setPostCode(String postCode) {
        this.postCode = postCode;
    }

    public String getEmpCode() {
        return empCode;
    }

    public void setEmpCode(String empCode) {
        this.empCode = empCode;
    }

    public String getEmpName() {
        return empName;
    }

    public void setEmpName(String empName) {
        this.empName = empName;
    }

    public String getDeptCode() {
        return deptCode;
    }

    public void setDeptCode(String deptCode) {
        this.deptCode = deptCode;
    }

    public String getDeptName() {
        return deptName;
    }

    public void setDeptName(String deptName) {
        this.deptName = deptName;
    }

    public String getCpyMark() {
        return cpyMark;
    }

    public void setCpyMark(String cpyMark) {
        this.cpyMark = cpyMark;
    }
}
